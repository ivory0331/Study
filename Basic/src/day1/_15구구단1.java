package day1;

public class _15备备窜1 { // 备备窜 1

	public static void main(String[] args) {
		
		int n = 2;
		int m = 3;
		
		for(int i=1; i<10; i++) {
			System.out.print(n+"*"+i+"="+(n*i));
			System.out.print("  ");
			System.out.print(m+"*"+i+"="+(m*i));
			System.out.println();
		}
		
	}

}
